package util;

public class Movement {
	
	public double front = 0;
	public double back = 0;
	public double right = 0;
	public double left = 0;
	
	public Movement() {
		super();
	}

	public Movement(double front, double back, double right, double left) {
		super();
		this.front = front;
		this.back = back;
		this.right = right;
		this.left = left;
	}
	
}
